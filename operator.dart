  const int num1 = 10;
  const int num2 = 20;
  int sum = num1 + num2;
  print(sum);

  var result = num2 - num1;
  print(result);

  int x = 10;
  x = x + 10;
  print(x);

  x = x - 5;
  print(x);

  if (x > 10) print(true);
  if (x < 10) print(false);

  result = num1 * num2;
  print(result);

  result *= 10;
  print(result);

  const y = 10 / 2;
  print(y);

  if (y <= 5) print(true);
  if (y <= 5) print(false);
  if (y != 5) print(true);
